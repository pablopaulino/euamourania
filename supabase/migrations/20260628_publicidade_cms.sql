create extension if not exists pgcrypto;

create table if not exists public.anunciantes (
  id uuid primary key default gen_random_uuid(),
  nome text not null,
  logo_url text,
  contato_email text,
  whatsapp text,
  status text not null default 'ativo' check (status in ('ativo','inativo')),
  usuario_id uuid references auth.users(id) on delete set null,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create table if not exists public.campanhas_publicitarias (
  id uuid primary key default gen_random_uuid(),
  nome text not null,
  anunciante_id uuid references public.anunciantes(id) on delete set null,
  empresa_anunciante text not null,
  logo_empresa_url text,
  tipo text not null default 'banner' check (tipo in ('banner','popup','video')),
  imagem_url text,
  video_url text,
  link_destino text,
  texto_botao text,
  abrir_nova_aba boolean not null default true,
  prioridade integer not null default 0,
  status text not null default 'rascunho' check (status in ('rascunho','ativo','pausado','encerrado')),
  data_inicio timestamptz,
  data_fim timestamptz,
  popup_uma_vez boolean not null default true,
  popup_reexibir text not null default '24h' check (popup_reexibir in ('24h','7d','sempre')),
  popup_atraso_seg integer not null default 3 check (popup_atraso_seg between 0 and 300),
  popup_botao_fechar boolean not null default true,
  configuracao_futura jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  constraint campanha_periodo_valido check (data_fim is null or data_inicio is null or data_fim >= data_inicio),
  constraint campanha_midia check (imagem_url is not null or video_url is not null or status = 'rascunho')
);

create table if not exists public.campanha_posicoes (
  campanha_id uuid not null references public.campanhas_publicitarias(id) on delete cascade,
  posicao text not null check (posicao in (
    'home_topo','home_hero_conteudo','home_entre_secoes','home_rodape',
    'noticias_topo','noticias_entre_listagem','noticia_meio','noticia_final',
    'guia_topo','guia_entre_estabelecimentos','guia_rodape',
    'turismo_topo','turismo_entre_cartoes','turismo_rodape',
    'eventos_topo','eventos_entre_eventos','eventos_rodape','todas_paginas'
  )),
  criado_em timestamptz not null default now(),
  primary key (campanha_id, posicao)
);

create table if not exists public.publicidade_metricas_diarias (
  id bigint generated by default as identity primary key,
  campanha_id uuid not null references public.campanhas_publicitarias(id) on delete cascade,
  dia date not null default current_date,
  impressoes bigint not null default 0 check (impressoes >= 0),
  cliques bigint not null default 0 check (cliques >= 0),
  ultima_visualizacao timestamptz,
  ultimo_clique timestamptz,
  unique (campanha_id, dia)
);

create table if not exists public.cms_atividades (
  id bigint generated by default as identity primary key,
  tabela text not null,
  registro_id text,
  acao text not null check (acao in ('criado','atualizado','excluido')),
  titulo text,
  usuario_id uuid references auth.users(id) on delete set null,
  dados jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now()
);

create index if not exists campanhas_publicitarias_status_periodo_idx on public.campanhas_publicitarias (status, data_inicio, data_fim);
create index if not exists campanhas_publicitarias_prioridade_idx on public.campanhas_publicitarias (prioridade desc);
create index if not exists campanha_posicoes_posicao_idx on public.campanha_posicoes (posicao);
create index if not exists publicidade_metricas_campanha_dia_idx on public.publicidade_metricas_diarias (campanha_id, dia desc);
create index if not exists cms_atividades_criado_idx on public.cms_atividades (criado_em desc);

create or replace function public.definir_atualizado_em()
returns trigger language plpgsql as $$
begin
  new.atualizado_em = now();
  return new;
end;
$$;

drop trigger if exists anunciantes_atualizado_em on public.anunciantes;
create trigger anunciantes_atualizado_em before update on public.anunciantes
for each row execute function public.definir_atualizado_em();

drop trigger if exists campanhas_atualizado_em on public.campanhas_publicitarias;
create trigger campanhas_atualizado_em before update on public.campanhas_publicitarias
for each row execute function public.definir_atualizado_em();

