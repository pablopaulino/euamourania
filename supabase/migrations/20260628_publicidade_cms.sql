-- Conversão compatível dos banners antigos. A origem é guardada para não duplicar.
do $$
begin
  if to_regclass('public.banners') is not null then
    execute $mig$
      insert into public.campanhas_publicitarias (
        nome, empresa_anunciante, tipo, imagem_url, video_url, link_destino,
        texto_botao, abrir_nova_aba, prioridade, status, data_inicio, data_fim,
        configuracao_futura
      )
      select
        coalesce(nullif(b.titulo,''),'Campanha importada'),
        coalesce(nullif(b.titulo,''),'Anunciante não informado'),
        case when b.video_url is not null and b.video_url <> '' then 'video' else 'banner' end,
        b.imagem_url, b.video_url, b.link_url, b.texto_botao, coalesce(b.nova_aba,true),
        coalesce(b.ordem,0), case when coalesce(b.ativo,false) then 'ativo' else 'pausado' end,
        b.inicio_em, b.fim_em, jsonb_build_object('banner_legado_id',b.id::text)
      from public.banners b
      where not exists (
        select 1 from public.campanhas_publicitarias c
        where c.configuracao_futura->>'banner_legado_id' = b.id::text
      )
    $mig$;

    execute $pos$
      insert into public.campanha_posicoes (campanha_id,posicao)
      select c.id,
        case b.posicao
          when 'home_topo' then 'home_topo'
          when 'home_meio' then 'home_entre_secoes'
          when 'home_rodape' then 'home_rodape'
          when 'noticias_topo' then 'noticias_topo'
          when 'noticias_meio' then 'noticias_entre_listagem'
          when 'noticia_individual_meio' then 'noticia_meio'
          when 'noticia_individual_final' then 'noticia_final'
          when 'guia_topo' then 'guia_topo'
          when 'guia_meio' then 'guia_entre_estabelecimentos'
          when 'turismo_topo' then 'turismo_topo'
          when 'turismo_meio' then 'turismo_entre_cartoes'
          when 'eventos_topo' then 'eventos_topo'
          else 'todas_paginas'
        end
      from public.campanhas_publicitarias c
      join public.banners b on c.configuracao_futura->>'banner_legado_id' = b.id::text
      on conflict do nothing
    $pos$;
  end if;
end $$;
