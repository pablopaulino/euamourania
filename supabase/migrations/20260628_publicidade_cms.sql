create or replace function public.registrar_evento_publicidade(
  p_campanha uuid,
  p_tipo text
) returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if p_tipo not in ('impressao','clique') then
    raise exception 'Tipo de evento inválido';
  end if;

  if not exists (
    select 1 from public.campanhas_publicitarias
    where id = p_campanha and status = 'ativo'
      and (data_inicio is null or data_inicio <= now())
      and (data_fim is null or data_fim >= now())
  ) then
    return;
  end if;

  insert into public.publicidade_metricas_diarias (
    campanha_id, dia, impressoes, cliques, ultima_visualizacao, ultimo_clique
  ) values (
    p_campanha, current_date,
    case when p_tipo = 'impressao' then 1 else 0 end,
    case when p_tipo = 'clique' then 1 else 0 end,
    case when p_tipo = 'impressao' then now() else null end,
    case when p_tipo = 'clique' then now() else null end
  )
  on conflict (campanha_id, dia) do update set
    impressoes = publicidade_metricas_diarias.impressoes + case when p_tipo = 'impressao' then 1 else 0 end,
    cliques = publicidade_metricas_diarias.cliques + case when p_tipo = 'clique' then 1 else 0 end,
    ultima_visualizacao = case when p_tipo = 'impressao' then now() else publicidade_metricas_diarias.ultima_visualizacao end,
    ultimo_clique = case when p_tipo = 'clique' then now() else publicidade_metricas_diarias.ultimo_clique end;
end;
$$;

revoke all on function public.registrar_evento_publicidade(uuid,text) from public;
grant execute on function public.registrar_evento_publicidade(uuid,text) to anon, authenticated;

create or replace function public.registrar_atividade_cms()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_registro jsonb;
  v_acao text;
  v_id text;
  v_titulo text;
begin
  if tg_op = 'DELETE' then
    v_registro := to_jsonb(old);
    v_acao := 'excluido';
  elsif tg_op = 'INSERT' then
    v_registro := to_jsonb(new);
    v_acao := 'criado';
  else
    v_registro := to_jsonb(new);
    v_acao := 'atualizado';
  end if;
  v_id := coalesce(v_registro->>'id','');
  v_titulo := coalesce(v_registro->>'titulo', v_registro->>'nome', v_registro->>'chave', v_id);
  insert into public.cms_atividades (tabela, registro_id, acao, titulo, usuario_id)
  values (tg_table_name, v_id, v_acao, v_titulo, auth.uid());
  return case when tg_op = 'DELETE' then old else new end;
end;
$$;

do $$
declare
  t text;
begin
  foreach t in array array['noticias','guia_comercial','turismo','eventos','links','campanhas_publicitarias']
  loop
    if to_regclass('public.' || t) is not null then
      execute format('drop trigger if exists cms_atividade_%I on public.%I', t, t);
      execute format('create trigger cms_atividade_%I after insert or update or delete on public.%I for each row execute function public.registrar_atividade_cms()', t, t);
    end if;
  end loop;
end $$;

create or replace view public.publicidade_resumo
with (security_invoker = true)
as
select
  c.id, c.nome, c.empresa_anunciante, c.tipo, c.status, c.prioridade,
  c.data_inicio, c.data_fim, c.criado_em, c.atualizado_em,
  case
    when c.status = 'encerrado' or (c.data_fim is not null and c.data_fim < now()) then 'encerrada'
    when c.status = 'ativo' and c.data_inicio is not null and c.data_inicio > now() then 'agendada'
    when c.status = 'ativo' then 'ativa'
    else c.status
  end as situacao,
  coalesce(sum(m.impressoes),0)::bigint as impressoes,
  coalesce(sum(m.cliques),0)::bigint as cliques,
  case when coalesce(sum(m.impressoes),0) = 0 then 0
       else round(sum(m.cliques)::numeric * 100 / sum(m.impressoes), 2) end as ctr,
  max(m.ultimo_clique) as ultimo_clique,
  max(m.ultima_visualizacao) as ultima_visualizacao
from public.campanhas_publicitarias c
left join public.publicidade_metricas_diarias m on m.campanha_id = c.id
group by c.id;

grant select on public.publicidade_resumo to authenticated;

