-- Importa banners antigos sem duplicar e usando somente colunas existentes no schema atual.
insert into public.campanhas_publicitarias(nome,empresa_anunciante,tipo,imagem_url,link_destino,prioridade,status,configuracao_futura)
select coalesce(nullif(b.titulo,''),'Campanha importada'),coalesce(nullif(b.titulo,''),'Anunciante não informado'),
 'banner',b.imagem_url,b.link_url,coalesce(b.ordem,0),case when b.status='ativo'then'ativo'else'pausado'end,
 jsonb_build_object('banner_legado_id',b.id::text)
from public.banners b where b.imagem_url is not null and not exists(
 select 1 from public.campanhas_publicitarias c where c.configuracao_futura->>'banner_legado_id'=b.id::text);
insert into public.campanha_posicoes(campanha_id,posicao)
select c.id,case b.posicao
 when'home_meio'then'home_entre_secoes' when'noticias_meio'then'noticias_entre_listagem'
 when'noticia_individual_meio'then'noticia_meio' when'noticia_individual_final'then'noticia_final'
 when'guia_meio'then'guia_entre_estabelecimentos' when'turismo_meio'then'turismo_entre_cartoes'
 when'home_topo'then'home_topo' when'home_rodape'then'home_rodape' when'noticias_topo'then'noticias_topo'
 when'guia_topo'then'guia_topo' when'turismo_topo'then'turismo_topo' when'eventos_topo'then'eventos_topo'
 else'todas_paginas'end
from public.campanhas_publicitarias c join public.banners b on c.configuracao_futura->>'banner_legado_id'=b.id::text
on conflict do nothing;
