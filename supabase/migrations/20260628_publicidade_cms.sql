create trigger anunciantes_atualizado_em before update on public.anunciantes for each row execute function public.definir_atualizado_em();
drop trigger if exists campanhas_atualizado_em on public.campanhas_publicitarias;
create trigger campanhas_atualizado_em before update on public.campanhas_publicitarias for each row execute function public.definir_atualizado_em();

